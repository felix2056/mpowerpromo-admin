    data() {
        return {
            isLoading: false,
            validationErrors: {},

            add_script_tag: {
                description: '',
                inner_html: ''
            }
        }
    },

    computed: {
        ...mapGetters({
            store: 'userstore/store',
        })
    },
    
    methods: {
        addTag() {
            this.isLoading = true

            axios.post(`/stores/head-tags/script/create`, this.add_script_tag, {
                headers: {
                    'X-Tenant-UUID': this.store.host
                }
            })
            .then(response => {
                this.isLoading = false
                this.$emit('tagAdded', response.data.script_tag)
            })
            .catch(error => {
                this.isLoading = false
                this.validationErrors = error.response.data.errors
            })
        },

        closeTag() {
            this.$emit('closeTag')
        }
    }
}
