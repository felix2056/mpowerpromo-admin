export default {
    props: {
        head_tag: {
            type: Object,
            required: true
        }
    },

    data() {
        return {
            isLoading: false,
            validationErrors: {},

            add_style_tag: {
                description: '',
                css_text: '',
                type: ''
            }
        }
    },

    computed: {
        ...mapGetters({
            store: 'userstore/store',
        })
    },
    
    methods: {
        addTag() {
            this.isLoading = true

            axios.post(`/stores/head-tags/style/create`, this.add_style_tag, {
                headers: {
                    'X-Tenant-UUID': this.store.host
                }
            })
            .then(response => {
                this.isLoading = false
                this.$emit('tagAdded', response.data.style_tag)
            })
            .catch(error => {
                this.isLoading = false
                this.validationErrors = error.response.data.errors
            })
        },

        closeTag() {
            this.$emit('closeTag')
        }
    }
}
