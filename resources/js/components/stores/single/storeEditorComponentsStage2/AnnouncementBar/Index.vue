<script>
import { mapGetters, mapActions } from 'vuex';

export default {
    name: 'StoreEditorSidebarStage2',

    data() {
        return {
            isLoading: false,
            validationErrors: {},

            announcement_bar: {},
            color_schemes: ['Scheme 1', 'Scheme 2']
        };
    },

    computed: {
        ...mapGetters({
            announcementBar: 'components/announcementBar',
        })
    },

    mounted() {
        this.get();
    },

    methods: {
        ...mapActions({
            update: 'components/update',
        }),

        get() {
            this.isLoading = true;

            axios.get(`/stores/theme/components/announcement-bar/${this.announcementBar.id}`)
                .then((response) => {
                    this.announcement_bar = response.data.announcement_bar;
                })
                .catch((error) => {
                    this.validationErrors = error.response.data.errors;
                })
                .finally(() => {
                    this.isLoading = false;
                });
        },

        update() {
            this.isLoading = true;
            this.validationErrors = {};

            axios.post(`/stores/theme/components/announcement-bar/update`, this.announcement_bar)
                .then((response) => {
                    this.validationErrors = {};
                    this.updateComponent({
                        'component': 'announcement-bar',
                        'data': response.data.announcement_bar
                    });
                })
                .catch((error) => {
                    this.validationErrors = error.response.data.errors;
                })
                .finally(() => {
                    this.isLoading = false;
                });
        },

        goBack() {
            this.$emit('goBack');
        }
    }
}
</script>

